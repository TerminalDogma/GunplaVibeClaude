// Utility functions for data
export const getAllGrades = () => {
  return [...new Set(SAMPLE_GUNPLA_DATA.map(m => m.grade))].sort();
};

export const getAllSeries = () => {
  return [...new Set(SAMPLE_GUNPLA_DATA.map(m => m.series))].sort();
};

export const getAllScales = () => {
  return [...new Set(SAMPLE_GUNPLA_DATA.map(m => m.scale))].sort();
};

export const getYearRange = () => {
  const years = SAMPLE_GUNPLA_DATA.map(m => m.releaseYear);
  return {
    min: Math.min(...years),
    max: Math.max(...years),
  };
};
